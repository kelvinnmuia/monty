/**
 * opc_add - gets the sum of two elements of the stack.
 * @head: stack head
 * @line_number: the line number
 * Return: returns nothing
*/
void opc_add(stack_t **head, unsigned int line_number)
{
	stack_t *h;
	int len = 0, aux;

	h = *head;
	while (h)
	{
		h = h->next;
		len++;
	}
	if (len < 2)
	{
		fprintf(stderr, "L%d: can't add, stack too short\n", line_number);
		fclose(bs.file);
		free(bs.content);
		inptr_free_stack(*head);
		exit(EXIT_FAILURE);
	}
	h = *head;
	aux = h->n + h->next->n;
	h->next->n = aux;
	*head = h->next;
	free(h);
}
/**
  *opc_sub- function that subtracts stack elements
  *@head: stack head
  *@line_number: the line number
  *Return: returns nothing
 */
void opc_sub(stack_t **head, unsigned int line_number)
{
	stack_t *aux;
	int sus, nodes;

	aux = *head;
	for (nodes = 0; aux != NULL; nodes++)
		aux = aux->next;
	if (nodes < 2)
	{
		fprintf(stderr, "L%d: can't sub, stack too short\n", line_number);
		fclose(bs.file);
		free(bs.content);
		inptr_free_stack(*head);
		exit(EXIT_FAILURE);
	}
	aux = *head;
	sus = aux->next->n - aux->n;
	aux->next->n = sus;
	*head = aux->next;
	free(aux);
}
